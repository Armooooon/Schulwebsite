<section class="about-school">
    <h1 class="h1ho">Hausordnung</h1>
    <p>Eine Schule ist wie ein Betrieb, in dem sich viele Menschen aufhalten und nach bestimmten Regeln bewegen müssen,
    damit niemand gefährdet oder verletzt wird und materielle Schäden vermieden werden.
    Um das zu erreichen, ist es nötig, bestimmte Regeln und Festlegungen zu kennen und einzuhalten. </p>
    <br>
    <p>
    <strong>1.1.</strong>
    Seid morgens pünktlich! Betretet aber bei schönem Wetter (kein Regen, starker Frost bzw. Schneefall)
    erst mit dem Gong in disziplinierter Art das Schulhaus durch den vorderen Eingang
    (Freitreppe-Schützenstraße, Klasse 1 und 2 über den Seiteneingang-Schulhof).
    </p>

    <p>
    <strong>1.2.</strong>
    Schüler, die mit dem Fahrrad bzw. mit einem Moped zur Schule kommen,
    stellen ihre Fahrzeuge auf dem dafür vorgesehenen Platz ab und betreten über den Haupteingang die Schule.
    Die Schule übernimmt keine Haftung für Mopeds, Fahrräder oder Zubehörteile.
    Das Befahren des Schulgeländes ist verboten.
    </p>

    <p>
    <strong>1.3.</strong>
    Die Haupteingangstür wird ab 14.00 Uhr verschlossen.
    Nach der 8.Stunde ist das Schulhaus über den Besuchereingang zu verlassen!
    Für das Verlassen des Hauses zur großen Pause benutzen wir nur die Ausgangtüren Hauptflügel Ost und Mittelausgang Ost.
    </p>

    <p>
    <strong>1.4.</strong>
    Bei schlechtem Wetter wird abgeklingelt.
    Alle Schüler begeben sich in die Unterrichtsräume nach Stundenplan und verbleiben bis zum Unterrichtsbeginn in diesem Raum.
    Nur in Absprache mit dem aufsichtsführenden Lehrer darf der Raum verlassen werden!
    </p>

    <p>
    <strong>1.5.</strong>
    Alle Schüler sind in der Schule, um zu lernen.
    Dabei wird nicht der Unterricht gestört!
    Hausaufgaben sind Pflichtaufgaben.
    Nicht angefertigte Hausaufgaben werden in der nächsten Unterrichtsstunde unaufgefordert nachgereicht.
    Anderenfalls drohen Konsequenzen durch schlechte Noten oder Ordnungsmaßnahmen.
    </p>

    <p>
    <strong>2.1.</strong>
    Achtet das Eigentum eurer Mitschüler!
    Essen, Kaugummi kauen, Trinken während des Unterrichtes ist untersagt.
    Ausnahmen entscheidet der Fachlehrer nach eigenem Ermessen.
    Werfen mit Gegenständen, Steinen bzw. Schneebällen ist strengstens untersagt.
    Mitschüler und Passanten zu belästigen bzw. parkende Fahrzeuge zu beschädigen, ist verboten.
    </p>

    <p>
    <strong>2.2.</strong>
    Während der Freistunden steht euch Raum 16, Raum 17, der Tischtennisraum und bis 11.00 Uhr der Speiseraum zur Verfügung.
    </p>

    <p>
    <strong>2.3.</strong>
    Wenn die Erziehungsberechtigten eine schriftliche Genehmigung (jeweils für ein Schuljahr bzw. auf Zeit) erteilen,
    dürfen Schüler nach Unterrichtsschluss (ab der 6. bzw. 7. Stunde) das Schulgelände verlassen und danach unbeaufsichtigt bleiben!
    In allen Schüleraufenthaltsräumen und an der Bushaltestelle wird durch eine Lehrkraft Aufsicht geführt.
    </p>

    <p>
    <strong>2.4.</strong>
    Die Unterrichtsstunde wird durch den Lehrer geschlossen, erst dann verlasst ihr,
    wenn es der Stundenplan vorschreibt, diesen Raum in einem ordentlichen Zustand,
    dass heißt der Klassenordner wischt gründlich die Tafel ab und alle Schüler räumen ihre Plätze auf.
    </p>

    <p>
    <strong>2.5.</strong>
    Nach letzter Unterrichtsstunde in diesem Raum werden die Stühle hochgestellt.
    Ist kein Raumwechsel nötig, wird die Zeit zur Vorbereitung auf das nächste Fach genutzt.
    </p>

    <p>
    <strong>2.6.</strong>
    Während des gesamten Unterrichtszeitraumes (erste bis letzte Stunde) darf das Schulgelände nur mit Genehmigung verlassen werden.
    (Versicherungsschutz bei eventuellen Unfällen)
    </p>

    <p>
    <strong>2.7.</strong>
    Aus Gründen der Gesundheitserhaltung entledigen wir uns im Schulhaus und während des Unterrichtes unserer Jacken,
    Kopfbedeckungen, Mäntel usw. und legen sie an den dafür vorgesehenen Plätzen ab.
    </p>

    <p>
    <strong>2.8.</strong>
    Wertgegenstände verbleiben bei euch. Höhere Geldbeträge lasst bitte zu Hause.
    MP3 Player, Discman, Telespiele und ähnliche Geräte dürfen im Unterricht nicht benutzt werden.
    Die Geräte, einschließlich Kopfhörer, sind während des Unterrichtes in den Schultaschen aufzubewahren.
    Mitgeführte Handys müssen ausgeschaltet in den Schultaschen verbleiben.
    Bei Nichteinhaltung ist die Lehrkraft berechtigt diese dem Schüler abzunehmen und im Sekretariat zu hinterlegen.
    Es erfolgt keine Haftung bei Beschädigung oder Verlust von Handys, Geräten und Gegenständen, die nicht zum Schulalltag gehören.
    Waffen, waffenähnliche Gegenstände wie Hieb-Schuss- und Stichwaffen, Messer jeglicher Art, KO-Spray, Elektroschocker, Schlagringe,
    Feuerwerkskörper gehören nicht in die Schule, da sie für den Lehr- und Lernbetrieb nicht notwendig sind.
    Bei Verstoß gegen diese Festlegung werden sofort die Eltern informiert,
    die dann diese von den Lehrkräften einbehaltenen Gegenstände in Empfang nehmen.
    Bei Schäden haften die Eltern.
    </p>

    <p>
    <strong>2.9.</strong>
    Falls ihr etwas vermisst oder findet, meldet euch in jedem Falle im Sekretariat bzw. beim Hausmeister.
    </p>

    <p>
    <strong>3.1.</strong>
    Das Öffnen und Schließen der Fenster ist Sache des Lehrers.
    Dabei ist es während der Heizperiode kostengünstiger, nur die großen Fenster für eine „Stoßlüftung“ (5-10min) geöffnet zu halten.
    </p>

    <p>
    <strong>3.2.</strong>
    Jede Klasse benennt wöchentlich einen Klassenordner in Abstimmung mit dem Klassenleiter.
    </p>

    <p>
    <strong>3.3.</strong>
    Die 9. und 10.Klassen bilden in jedem Jahr eine Ordnungsgruppe, die befugt ist,
    die Einhaltung der Hausordnung durchzusetzen. Während der Prüfungszeit übernehmen Schüler der 9. Klassen die Aufgaben der Ordnungsgruppe.
    </p>

    <p>
    <strong>3.4.</strong>
    Haltet eure Toiletten sauber! Toiletten sind keine Pausenräume!
    Um Ordnung auf dem Schulgelände zu gewährleisten, sind die aufgestellten Abfallbehälter zu nutzen. Blumentöpfe sind keine Abfallbehälter!
    Festgestellte Schäden am Mobiliar oder dem Gebäude, vor allem beim Betreten des Unterrichtsraumes sind sofort dem Fachlehrer zu melden.
    </p>

    <p>
    <strong>3.5.</strong>
    Im gesamten Schulgebäude ist der Genuss von Suchtmitteln untersagt. Bei Zuwiderhandlung erfolgt eine polizeiliche Anzeige.
    Im und auf dem Schulgelände herrscht Rauchverbot!
    Bei Zuwiderhandlung werden Ordnungsmaßnahmen eingeleitet und den Eltern zur Kenntnis gegeben.
    </p>
    <p>
    <strong>3.6.</strong>
    Solltet ihr zu Beginn einer Stunde keinen Lehrer haben, informiert der Klassensprecher bzw. stellv. Klassensprecher sofort die Schulleitung.
    </p>

    <p>
    <strong>3.7.</strong>
    Vermeidet Unfälle durch überlegtes und rücksichtsvolles Bewegen besonders auf Treppen und auf dem Schulhof.
    Unterlasst das Toben und Lärmen im Gebäude!
    </p>

    <p>
    <strong>3.8.</strong>
    Demminer Schüler haben nach Beendigung des regulären Unterrichts bzw. von anderen Schulveranstaltungen
    das Schulgelände unverzüglich zu verlassen und sich auf dem kürzesten bzw. sichersten Weg nach Hause zu begeben.
    Sie haben sonst bei eventuellen Unfällen keinen Versicherungsschutz.
    </p>

    <p>
    <strong>3.9.</strong>
    Schüler, die an der Schulspeisung teilnehmen, sorgen bitte durch diszipliniertes Verhalten dafür,
    dass die Esseneinnahme geordnet und in ruhiger Atmosphäre abläuft.
    Die Essenausgabe beginnt um 11.20 Uhr.
    Schüler, die in der 6. Stunde Sportunterricht haben, essen nach Unterrichtsschluss.
    Die nicht an der Schulspeisung teilnehmenden Schüler haben sich nicht während der Essenszeit im Speiseraum aufzuhalten.
    </p>

    <p>
    <strong>4.0.</strong>
    Es versteht sich wohl von selbst, dass wir unseren Grundschülern hilfsbereit begegnen und Streitigkeiten vermeiden.
    Gegenseitige Rücksichtnahme bedeutet auch, Mitschüler nicht zu schlagen.
    Das gilt insbesondere für alle Fahrschüler beim Einsteigen in den Schülerbus.
    Denkt bitte in eurem gesamten Verhalten daran, dass für eure Einschätzung nur eure Taten zählen und nicht Versprechungen.
    </p>

    <p>
    <strong>4.1.</strong>
    Außerdem werdet ihr wohl verstehen, dass mutwillige Verstöße gegen die Hausordnung
    den gesamten schulischen Ablauf gefährden und das Klima an unserer Schule verschlechtern
    und somit zu Ordnungs- und Erziehungsmaßnahmen führen.
    </p>

    <p>
    <strong>4.2.</strong>
    Die Lehrer sind befugt den Schülern Gegenstände, die den Unterricht oder die Ordnung der Schule stören können, wegzunehmen und sicherzustellen.
    Über die Form der Rückgabe entscheidet der Klassen- bzw. Schulleiter.
    </p>

    <p>
    <strong>4.3. Hausrecht</strong>

    <br>
    Der Schulleiter übt das Hausrecht im:
<ul>
    <li>gesamten Bereich der Regionalen Schule aus.</li>
    <li>der Lehrer übt in seinem Unterrichtsraum und in seinem Aufsichtsbereich das Hausrecht aus.</li>
    <li>schulfremde Personen dürfen sich nur nach Anmeldung im Sekretariat in der Schule aufhalten. </li>
</ul>
    <i>Demmin, Februar 2015 </i>
    </p>

    <br>
    <p><strong>Das erwartet mich, wenn ich einzelne Festlegungen der Hausordnung nicht einhalte!!! </strong></p>
