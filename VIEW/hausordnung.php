    <li>Bei Nichtbefolgen der Aufforderung des Lehrers (Pkt. 2.8.) greifen Ordnungsmaßnahmen.</li>
    <li><strong>Erneut eingezogene Handys werden durch die Eltern abgeholt!!!</strong></li>
    <li>Verstoß gegen Pkt. 3.5. (Drogen, Alkohol, Rauchen) - <strong>sofortige Anzeige bei der Polizei!!!</strong></li>
</ul>





