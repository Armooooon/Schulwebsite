Ein Schulsprecher ist eine gewählte oder ernannte Schülervertretung, die die Interessen und Anliegen ihrer Mitschülerinnen und Mitschüler gegenüber der Schulleitung, Lehrern und anderen Institutionen vertritt. Ihre Aufgaben umfassen die Förderung des Schülerengagements, die Organisation von schulischen Veranstaltungen, die Lösung von Problemen und Konflikten sowie die Förderung eines positiven und inklusiven Schulklimas. Der Schulsprecher dient als Bindeglied zwischen Schülern und der Schulverwaltung und arbeitet daran, die Schülerstimme in Entscheidungsprozessen zu stärken und die Schülergemeinschaft zu stärken.</p>
